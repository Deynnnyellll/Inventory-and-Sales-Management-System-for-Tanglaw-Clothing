    public void setCustomerDetailsToTable(){
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/tanglaw?serverTimezone=UTC&useSSL=false", "root", "12345678");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from customer_details order by customer");
            
            while(rs.next()){
                String idcustomer = rs.getString("idcustomer");
                String customer = rs.getString("customer");
                String email = rs.getString("email");
                String gender  = rs.getString("gender");
                String birthday= rs.getString("birthday");
                String shipping = rs.getString("shipping_address");
                
                Object[] obj = {idcustomer,customer,email,gender,birthday,shipping};
                model =(DefaultTableModel) tablecustomer.getModel();
                model.addRow(obj);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }
    
     
   
